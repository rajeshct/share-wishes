        if (remoteMessage != null) {
            if (remoteMessage.data.isNotEmpty()) {
            }
        }

        // Check if message contains a notification payload.
        if (remoteMessage != null) {
            val tempNotification = remoteMessage.notification
            if (tempNotification != null) {
                val title = if (tempNotification.title == null) getString(R.string.app_name) else tempNotification.title
